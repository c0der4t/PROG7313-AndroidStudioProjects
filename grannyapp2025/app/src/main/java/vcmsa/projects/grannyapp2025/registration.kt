class registration : AppCompatActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContentView(R.layout.activity_registration)
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main)) { v, insets ->
            val systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars())
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom)
            insets
        }

        var usernameEdit : TextView = findViewById(R.id.edtRegistration_username)
        var passwordEdit : TextView = findViewById(R.id.edtRegistration_password)
        var passwordConfirmEdit : TextView = findViewById(R.id.edtRegistration_confirmpassword)

    }
}